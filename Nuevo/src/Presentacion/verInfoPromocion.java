    public String getNombrePromo(String cadena){
    
        String [] partes = new String[2];

        partes = cadena.split("-");

        String nombre;

        nombre = String.valueOf(partes[0].trim());

        return nombre;
    }
        public String getNombreProv(String cadena){
    
        String [] partes = new String[2];

        partes = cadena.split("-");

        String nombre;

        nombre = String.valueOf(partes[1].trim());

        return nombre;
    }

